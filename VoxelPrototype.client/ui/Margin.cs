namespace VoxelPrototype.client.ui
{
    public struct Margin
    {
        public int Left;
        public int Right;
        public int Top;
        public int Bottom;

        public Margin(int left, int right, int top, int bottom)
        {
            Left = left;
            Right = right;
            Top = top;
            Bottom = bottom;
        }
    }
}
